    def generate_h1(self, city_name):
        """生成H1标题"""
        template = self.h1_templates.get(city_name, "{city}宠物火化服务避坑指南：安心告别与费用解析")
        return template.format(city=city_name)
    
    def fix_file(self, file_path, city_name):
        """修复单个文件"""
        try:
            with open(file_path, 'r', encoding='utf-8') as f:
                content = f.read()
            
            original = content
            
            # 生成新的H1
            new_h1 = self.generate_h1(city_name)
            
            # 替换H1
            content = re.sub(r'\n#\s*[^\n]+\n', f'\n# {new_h1}\n', content, count=1)
            
            # 更新title（截取H1的前部分作为title核心）
            # 从H1中提取城市名+核心服务
            title_core = re.search(r'^([^：]+)', new_h1).group(1) if '：' in new_h1 else f"{city_name}宠物火化服务"
            
            # 更新title，保持原有格式但使用新的核心
            new_title = f"{title_core}|附近宠物火化|附近宠物殡葬 - 百情宠物善终"
            content = re.sub(r'^title:\s*[^\n]+', f'title: {new_title}', content, flags=re.MULTILINE)
            
            if content != original:
                with open(file_path, 'w', encoding='utf-8') as f:
                    f.write(content)
                return True
            return False
            
        except Exception as e:
            print(f"❌ 修复失败 {file_path}: {e}")
            return False
    
    def run(self):
        """执行修复"""
        print("开始以H1为主修复title...\n")
        
        city_files = glob.glob(str(self.base_dir / 'docs' / 'cities' / '*' / '*' / 'index.md'))
        
        for file_path in sorted(city_files):
            city_name = Path(file_path).parent.name
            self.stats['checked'] += 1
            
            if self.fix_file(file_path, city_name):
                print(f"✅ 已修复: {city_name}")
                self.stats['fixed'] += 1
        
        print(f"\n{'='*50}")
        print(f"检查文件数: {self.stats['checked']}")
        print(f"修复文件数: {self.stats['fixed']}")
        print(f"{'='*50}")

