    def optimize_all_cities(self):
        """优化所有城市页面"""
        city_dirs = glob.glob(str(self.base_dir / 'docs' / 'cities' / '*'))
        
        print(f"发现 {len(city_dirs)} 个城市目录")
        print("开始批量优化...\n")
        
        for city_dir in sorted(city_dirs):
            city_name = Path(city_dir).name
            file_path = Path(city_dir) / 'index.md'
            
            if not file_path.exists():
                print(f"⚠️ 跳过 {city_name}: 无index.md")
                self.stats['skipped'] += 1
                continue
            
            try:
                with open(file_path, 'r', encoding='utf-8') as f:
                    content = f.read()
                
                original_content = content
                modified = False
                
                # 1. 添加城市独特内容（如果还没有）
                if "宠物火化服务由百情宠物善终提供" not in content:
                    template = self.city_templates.get(
                        city_name, 
                        "百情宠物善终为您提供专业的宠物火化服务，24小时上门，费用透明。"
                    )
                    
                    # 在frontmatter后添加
                    lines = content.split('\n')
                    new_lines = []
                    for i, line in enumerate(lines):
                        new_lines.append(line)
                        if line.strip() == '---' and i > 0:
                            if i < len(lines) - 1 and lines[i+1].strip().startswith('#'):
                                unique_content = f"\n{city_name}宠物火化服务由百情宠物善终提供专业支持。{template}\n"
                                new_lines.append(unique_content)
                                modified = True
                                self.stats['added_content'] += 1
                    
                    if modified:
                        content = '\n'.join(new_lines)
                
                # 2. 融入"宠物火葬"关键词（如果还没有）
                if '宠物火葬' not in content and '宠物火化' in content:
                    # 将部分"火化"替换为"火葬"（约20%）
                    huohua_count = content.count('宠物火化')
                    if huohua_count > 2:
                        # 随机替换1-2处
                        replace_count = min(2, huohua_count // 3)
                        content = content.replace('宠物火化', '宠物火葬', replace_count)
                        self.stats['added_huozang'] += 1
                        modified = True
                
                # 3. 保存修改
                if modified:
                    with open(file_path, 'w', encoding='utf-8') as f:
                        f.write(content)
                    print(f"✅ 已优化: {city_name}")
                else:
                    print(f"⏭️ 跳过: {city_name} (已优化)")
                    
                self.stats['processed'] += 1
                
            except Exception as e:
                print(f"❌ 失败 {city_name}: {e}")
                self.stats['skipped'] += 1
        
        self.print_report()
    
    def print_report(self):
        """打印优化报告"""
        print("\n" + "="*50)
        print("城市页面优化完成报告")
        print("="*50)
        print(f"处理城市数: {self.stats['processed']}")
        print(f"添加独特内容: {self.stats['added_content']}")
        print(f"融入火葬关键词: {self.stats['added_huozang']}")
        print(f"跳过/失败: {self.stats['skipped']}")
        print("="*50)

