def check_district_links():
    """检查DistrictList组件生成的区县链接"""
    base_dir = '/home/mz/.openclaw/workspace/pet-funeral/docs/cities'
    
    # 获取所有存在的区县文章
    existing_districts = {}
    for root, dirs, files in os.walk(base_dir):
        for file in files:
            if file.endswith('.md') and '宠物火化服务' in file and file != 'index.md':
                filepath = os.path.join(root, file)
                rel_path = os.path.relpath(filepath, base_dir)
                parts = rel_path.split(os.sep)
                if len(parts) == 3:  # 省份/城市/区县.md
                    province, city, district_file = parts
                    district_name = district_file.replace('宠物火化服务.md', '')
                    key = f"{province}/{city}/{district_name}"
                    existing_districts[key] = filepath
    
    # 检查每个城市的区县链接
    missing_districts = []
    
    # 遍历实际存在的城市目录
    for province in os.listdir(base_dir):
        province_path = os.path.join(base_dir, province)
        if not os.path.isdir(province_path) or province == 'index.md':
            continue
        
        for city in os.listdir(province_path):
            city_path = os.path.join(province_path, city)
            if not os.path.isdir(city_path):
                continue
            
            # 检查这个城市在DistrictList中定义的区县
            if city in CITY_DISTRICTS:
                expected_districts = CITY_DISTRICTS[city]
                for district in expected_districts:
                    key = f"{province}/{city}/{district}"
                    if key not in existing_districts:
                        missing_districts.append({
                            'province': province,
                            'city': city,
                            'district': district,
                            'expected_file': f"{city_path}/{district}宠物火化服务.md"
                        })
    
    return missing_districts, existing_districts

def main():
    missing, existing = check_district_links()
    
    print("=" * 80)
    print("DistrictList区县链接检查报告")
    print("=" * 80)
    print()
    
    print(f"📊 统计信息")
    print("-" * 80)
    print(f"已存在的区县文章: {len(existing)}")
    print(f"缺失的区县文章: {len(missing)}")
    print()
    
    if missing:
        print("🔴 缺失的区县文章（DistrictList会生成404链接）")
        print("-" * 80)
        
        # 按城市分组
        by_city = {}
        for item in missing:
            key = f"{item['province']}/{item['city']}"
            if key not in by_city:
                by_city[key] = []
            by_city[key].append(item['district'])
        
        for city_key, districts in sorted(by_city.items())[:20]:  # 只显示前20个城市
            print(f"\n{city_key}:")
            for district in districts[:10]:  # 每个城市最多显示10个
                print(f"  - {district}")
            if len(districts) > 10:
                print(f"  ... 还有 {len(districts) - 10} 个")
        
        if len(by_city) > 20:
            print(f"\n... 还有 {len(by_city) - 20} 个城市")
    else:
        print("✅ 所有DistrictList链接对应的区县文章都存在！")
    
    print()
    print("=" * 80)

if __name__ == '__main__':
    main()
